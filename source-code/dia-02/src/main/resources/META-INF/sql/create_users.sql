CREATE TABLE USERS (
    USERNAME VARCHAR(50) PRIMARY KEY,
    PASSWORD VARCHAR(50)
);

CREATE TABLE USER_GROUPS (
    USERNAME VARCHAR(50),
    GROUPNAME VARCHAR(50),
    PRIMARY KEY (USERNAME, GROUPNAME),
    FOREIGN KEY (USERNAME) REFERENCES USERS(USERNAME)
);

